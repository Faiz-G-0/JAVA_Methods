public class while_loop {
    public static void main(String[] args)
    {
        int i =1;
     
        while (i<=5) {
            System.out.println(i+".Hello");
            int j=1;
            while (j<=3) {
                System.out.println(j+"Hii");
                j++;
            }
            i++;    
        }
          
    }
    
}
